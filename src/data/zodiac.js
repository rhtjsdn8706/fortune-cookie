function getZodiacDaySeed() {
  const today = new Date();
  return today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate();
}

function seededRandom(seed) {
  let x = Math.sin(seed) * 10000;
  return x - Math.floor(x);
}

export function getZodiac(year) {
  const index = ((year - 4) % 12 + 12) % 12;
  return zodiacAnimals[index];
}

export function getZodiacFortune(year) {
  const index = ((year - 4) % 12 + 12) % 12;
  const fortunes = zodiacFortunes[index];
  const daySeed = getZodiacDaySeed();
  // 띠 인덱스와 날짜로 시드 생성 → 같은 띠 + 같은 날 = 같은 결과
  const fortuneIndex = Math.floor(seededRandom(daySeed + index) * fortunes.length);
  return fortunes[fortuneIndex];
}
